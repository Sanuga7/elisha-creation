    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String email = jTextField1.getText();
        String pwd = String.valueOf(jPasswordField1.getPassword());
        
        if(UserValidate.isEmailValid(email)){
            if(UserValidate.isPwdValid(pwd)){
               if(User.userLogin(email, pwd)){
                    Message.showSucess(this, "Login Sucessfull");
                    Home home = new Home();
                    home.setVisible(true);
                    this.dispose();
                }else{
                  Message.showError(this, "Login Failed Check Email And Password");
                } 
            }else{
               Message.showError(this, "Invalid Password");
            }
        }else{
          Message.showError(this, "Invalid Email Address");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

